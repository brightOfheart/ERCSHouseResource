    public static Bitmap getBitmapFromUrl(String url, double width, double height) {
        BitmapFactory.Options options = new BitmapFactory.Options();
        options.inJustDecodeBounds = true; // 设置了此属性一定要记得将值设置为false
        Bitmap bitmap = BitmapFactory.decodeFile(url);
        // 防止OOM发生
        options.inJustDecodeBounds = false;
        int mWidth = bitmap.getWidth();
        int mHeight = bitmap.getHeight();
        float m=1;
        Matrix matrix = new Matrix();
        float scaleWidth = 1;
        float scaleHeight = 1;
        double w,h;
        if(width>height)
        {
            w=height;
            h=width;
            // 按照固定宽高进行缩放
            if(mWidth <= mHeight) {
                scaleWidth = (float) (w/mWidth);
                scaleHeight = (float) (h/mHeight);
            } else {
                scaleWidth = (float) (h/mWidth);
                scaleHeight = (float) (w/mHeight);
            }
        }
        else
        {
            // 按照固定宽高进行缩放
            if(mWidth <= mHeight) {
                scaleWidth = (float) (width/mWidth);
                scaleHeight = (float) (height/mHeight);
            } else {
                scaleWidth = (float) (height/mWidth);
                scaleHeight = (float) (width/mHeight);
            }
        }


        // 按照固定大小对图片进行缩放
        matrix.postScale(scaleWidth, scaleHeight);
        Bitmap newBitmap = Bitmap.createBitmap(bitmap, 0, 0, mWidth, mHeight, matrix, true);
        // 用完了记得回收
        bitmap.recycle();
        return newBitmap;
    }
    public static String BitmapToString(Bitmap bitmap) {
        String des = null;
        try {
            ByteArrayOutputStream out = new ByteArrayOutputStream();
            bitmap.compress(Bitmap.CompressFormat.JPEG, 90, out);
            out.flush();
            out.close();
            byte[] buffer = out.toByteArray();
            byte[] encode = Base64.encode(buffer, Base64.DEFAULT);
            des = new String(encode);
        } catch (Exception e) {
            e.printStackTrace();
        }
        return des;
    }

